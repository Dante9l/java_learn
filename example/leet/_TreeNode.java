package leet;

public class _TreeNode {
    int val;
    _TreeNode left;
    _TreeNode right;
    _TreeNode() {}
    _TreeNode(int val) { this.val = val; }
    _TreeNode(int val, _TreeNode left, _TreeNode right) {
          this.val = val;
          this.left = left;
          this.right = right;
    }
}
